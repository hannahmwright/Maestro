import type { ToolType } from '../../types';

export function hasSupportedProviderIcon(toolType?: ToolType | null): boolean {
	return (
		toolType === 'codex' ||
		toolType === 'claude-code' ||
		toolType === 'opencode' ||
		toolType === 'factory-droid' ||
		toolType === 'terminal'
	);
}

export function getProviderBrandColor(
	toolType?: ToolType | null,
	fallback = 'currentColor'
): string {
	switch (toolType) {
		case 'claude-code':
			return '#D97757';
		case 'codex':
			return fallback;
		case 'opencode':
			return '#F97316';
		case 'factory-droid':
			return '#3B82F6';
		default:
			return fallback;
	}
}

